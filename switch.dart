void main() {
  var days = 5;
  switch (days) {
    case 1:
      print("monday");

    case 2:
      print("thrusday");

    case 3:
      print("wendesday");

    case 4:
      print("thusday");

    case 5:
      print("friday");

    default:
      print("it is a weekend");
  }
}
